SELECT r.routeName, s1.stationName AS StartStation, s2.stationName AS EndStation, r.distance
FROM Route r
JOIN Station s1 
    ON r.startStationID = s1.stationID
JOIN Station s2 
    ON r.endStationID = s2.stationID;

-- 2. List all Tickets with their respective Passenger names:
SELECT t.ticketNumber, p.passengerName, t.price, t.type
FROM Ticket t
JOIN Passenger p 
    ON t.passengerID = p.passengerID;

-- 3. Show Schedule details along with the Driver's name:
SELECT s.scheduleID, d.driverName, s.frequency
FROM Schedule s
JOIN Driver d 
    ON s.driverID = d.driverID;

-- 4. Display all Vehicles and their Bus details:
SELECT v.model, v.capacity, b.busNumber, b.busType
FROM Vehicle v
LEFT JOIN Bus b 
    ON v.vehicleID = b.vehicleID;

-- 5. Find which Passenger is traveling on which Route:
SELECT p.passengerName, r.routeName
FROM Ticket t
JOIN Passenger p 
    ON t.passengerID = p.passengerID
JOIN Schedule s 
    ON t.scheduleID = s.scheduleID
JOIN Route r 
    ON s.routeID = r.routeID;

-- 6. Get Total Revenue generated by each Route:
SELECT r.routeName, SUM(t.price) AS TotalRevenue
FROM Ticket t
JOIN Schedule s 
    ON t.scheduleID = s.scheduleID
JOIN Route r 
    ON s.routeID = r.routeID
GROUP BY r.routeName;

-- 7. List all Drivers and the Vehicles they are scheduled to drive:
SELECT d.driverName, v.model, s.frequency
FROM Schedule s
JOIN Driver d 
    ON s.driverID = d.driverID
JOIN Vehicle v 
    ON s.vehicleID = v.vehicleID;






-- Views



-- 1. View of Active Routes:
CREATE OR REPLACE VIEW ActiveRoutes_VW AS
SELECT r.routeID, r.routeName, r.distance, s.stationName AS StartPoint
FROM Route r
JOIN Station s 
    ON r.startStationID = s.stationID;

-- 2. View of Passenger Ticket History:
CREATE OR REPLACE VIEW TicketHistory_VW AS
SELECT p.passengerName, t.ticketNumber, t.price, t.issueDate
FROM Passenger p
JOIN Ticket t 
    ON p.passengerID = t.passengerID;

-- 3. View of Highly Experienced Drivers:
CREATE OR REPLACE VIEW SeniorDrivers_VW AS
SELECT driverName, licenseNumber, experience
FROM Driver
WHERE experience > 10;

-- 4. View of the Train Fleet:
CREATE OR REPLACE VIEW TrainFleet_VW AS
SELECT v.vehicleID, v.model, v.capacity, t.trainNumber, t.numberOfCoaches
FROM Vehicle v
JOIN Train t 
    ON v.vehicleID = t.vehicleID;

-- 5. View of Daily Schedules:
CREATE OR REPLACE VIEW DailySchedules_VW AS
SELECT scheduleID, arrivalTime, departureTime, routeID
FROM Schedule
WHERE frequency = 'Daily';

-- 6. View of Station Analytics:
CREATE OR REPLACE VIEW StationAnalytics_VW AS
SELECT s.stationName, COUNT(r.routeID) AS TotalRoutesStartingHere
FROM Station s
LEFT JOIN Route r 
    ON s.stationID = r.startStationID
GROUP BY s.stationName;

-- 7. View of VIP Passengers:
CREATE OR REPLACE VIEW VIP_Passengers_VW AS
SELECT DISTINCT p.passengerName, p.phoneNumber, t.type
FROM Passenger p
JOIN Ticket t 
    ON p.passengerID = t.passengerID
WHERE t.type = 'VIP';




