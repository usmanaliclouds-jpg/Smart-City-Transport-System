-- 1. Procedure with IN Parameter to Add a New Passenger:
CREATE OR REPLACE PROCEDURE Add_Passenger (
    p_id IN NUMBER, 
    p_name IN VARCHAR2, 
    p_email IN VARCHAR2, 
    p_phone IN VARCHAR2,
    p_dob IN DATE,
    p_age IN NUMBER
) IS
BEGIN
    INSERT INTO Passenger (
        passengerID, passengerName, passengerEmail, phoneNumber, DOB, age
    ) VALUES (
        p_id, p_name, p_email, p_phone, p_dob, p_age
    );
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('New passenger added successfully.');
END Add_Passenger;
/








-- 2. Procedure with an OUT Parameter to count tickets for a specific schedule:
CREATE OR REPLACE PROCEDURE Count_Schedule_Tickets (
    p_scheduleID IN NUMBER, 
    v_total_tickets OUT NUMBER
) IS
BEGIN
    SELECT COUNT(*) 
    INTO v_total_tickets 
    FROM Ticket 
    WHERE scheduleID = p_scheduleID;
END Count_Schedule_Tickets;
/






-- 3. Procedure with an IN OUT Parameter to calculate and return a discounted price:
CREATE OR REPLACE PROCEDURE Apply_Special_Discount (
    p_ticketID IN NUMBER, 
    p_price_mod IN OUT NUMBER
) IS
BEGIN
    -- Subtracting the IN OUT value from current price
    UPDATE Ticket
    SET price = price - p_price_mod
    WHERE ticketNumber = p_ticketID;
    
    -- Fetching the updated price to send back via the same IN OUT parameter
    SELECT price 
    INTO p_price_mod 
    FROM Ticket 
    WHERE ticketNumber = p_ticketID;
    
    COMMIT;
END Apply_Special_Discount;
/

-- 4. Procedure using a FOR Loop to print all drivers with high experience:
CREATE OR REPLACE PROCEDURE Print_Senior_Drivers (
    p_min_experience IN NUMBER
) IS
BEGIN
    DBMS_OUTPUT.PUT_LINE('Senior Drivers List:');
    FOR i IN (
        SELECT driverName, experience 
        FROM Driver 
        WHERE experience >= p_min_experience
    ) 
    LOOP
        DBMS_OUTPUT.PUT_LINE(i.driverName || ' - ' || i.experience || ' years');
    END LOOP;
END Print_Senior_Drivers;
/

-- 5. Procedure using SELECT INTO to get and print a Passenger's Email:
CREATE OR REPLACE PROCEDURE Print_Passenger_Email (
    p_passID IN NUMBER
) IS
    v_email VARCHAR2(100);
BEGIN
    SELECT passengerEmail 
    INTO v_email 
    FROM Passenger 
    WHERE passengerID = p_passID;
    
    DBMS_OUTPUT.PUT_LINE('Passenger Email: ' || v_email);
END Print_Passenger_Email;
/

-- 6. Procedure to safely cancel a ticket and free up schedule space:
CREATE OR REPLACE PROCEDURE Cancel_Passenger_Ticket (
    p_ticket_num IN NUMBER
) IS
BEGIN
    DELETE FROM Ticket 
    WHERE ticketNumber = p_ticket_num;
    
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Ticket ' || p_ticket_num || ' has been cancelled.');
END Cancel_Passenger_Ticket;
/

-- 7. Procedure using a FOR Loop to show all routes starting from a specific station:
CREATE OR REPLACE PROCEDURE Print_Station_Routes (
    p_stationID IN NUMBER
) IS
BEGIN
    DBMS_OUTPUT.PUT_LINE('Routes starting from Station ID: ' || p_stationID);
    FOR i IN (
        SELECT routeName, distance 
        FROM Route 
        WHERE startStationID = p_stationID
    ) 
    LOOP
        DBMS_OUTPUT.PUT_LINE('Route: ' || i.routeName || ' | Distance: ' || i.distance || 'km');
    END LOOP;
END Print_Station_Routes;




