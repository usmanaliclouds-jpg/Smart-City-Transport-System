-- Trigger 




-- 1. Trigger to automatically enforce a minimum ticket price of 50:
CREATE OR REPLACE TRIGGER trg_enforce_min_price
BEFORE INSERT OR UPDATE ON Ticket
FOR EACH ROW
BEGIN
    IF :NEW.price < 50 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Error: Minimum ticket price must be at least Rs. 50.');
    END IF;
END;
/

-- 2. Trigger to dynamically calculate Passenger Age based on DOB on insert:
CREATE OR REPLACE TRIGGER trg_auto_calc_age
BEFORE INSERT OR UPDATE ON Passenger
FOR EACH ROW
BEGIN
    IF :NEW.DOB IS NOT NULL THEN
        :NEW.age := TRUNC(MONTHS_BETWEEN(SYSDATE, :NEW.DOB) / 12);
    END IF;
END;
/

-- 3. Trigger to prevent a Schedule from having the same Start and End time:
CREATE OR REPLACE TRIGGER trg_prevent_zero_duration
BEFORE INSERT OR UPDATE ON Schedule
FOR EACH ROW
BEGIN
    IF :NEW.arrivalTime = :NEW.departureTime THEN
        RAISE_APPLICATION_ERROR(-20002, 'Error: Arrival and Departure times cannot be identical.');
    END IF;
END;
/

-- 4. Trigger to ensure Bus Names are always stored in UPPERCASE:
CREATE OR REPLACE TRIGGER trg_uppercase_bus_number
BEFORE INSERT OR UPDATE ON Bus
FOR EACH ROW
BEGIN
    :NEW.busNumber := UPPER(:NEW.busNumber);
END;
/

-- 5. Trigger to default the Ticket Type to 'Standard' if user forgets to enter one:
CREATE OR REPLACE TRIGGER trg_default_ticket_type
BEFORE INSERT ON Ticket
FOR EACH ROW
BEGIN
    IF :NEW.type IS NULL THEN
        :NEW.type := 'Standard';
    END IF;
END;
/

-- 6. Trigger to prevent a Route distance from being updated to a smaller value:
CREATE OR REPLACE TRIGGER trg_prevent_distance_shrink
BEFORE UPDATE ON Route
FOR EACH ROW
BEGIN
    IF :NEW.distance < :OLD.distance THEN
        RAISE_APPLICATION_ERROR(-20003, 'Error: Route distance cannot be decreased once established.');
    END IF;
END;
/

-- 7. Trigger to ensure new Drivers have at least 1 year of experience:
CREATE OR REPLACE TRIGGER trg_minimum_driver_experience
BEFORE INSERT ON Driver
FOR EACH ROW
BEGIN
    IF :NEW.experience < 1 THEN
        RAISE_APPLICATION_ERROR(-20004, 'Error: Driver must have at least 1 year of experience.');
    END IF;
END;





